public class words {
    public String name;
    public String string;
    public words(String a, String b)
    {
        name = a;
        string=b;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }
}
